    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {  
        firstname = jTextField1.getText();
        lastname = jTextField2.getText();
        phone = jTextField3.getText();
        address = jTextField4.getText();
        zip = jTextField5.getText();
        email = jTextField6.getText();
        cc = jTextField8.getText();
        security = jTextField7.getText();

        // set next page - return to main menu
        OrderXML.initialize();
        OrderXML.readXML();

        OrderXML.customer.firstName = firstname;
        OrderXML.customer.lastName = lastname;
        OrderXML.customer.phone = phone;
        OrderXML.customer.address = address;
        OrderXML.customer.zip = zip;
        OrderXML.customer.email = email;
        OrderXML.customer.cc = cc;
        OrderXML.customer.secCode = security;

        OrderXML.saveXML();

        ((CardLayout)mainPanel.getLayout()).show(mainPanel, "TopLevelMenu");
    }                                        


